def main():
    print(f"--- 処理開始: {INPUT_FILE} ---")

    if not os.path.exists(INPUT_FILE):
        print(f"エラー: {INPUT_FILE} が見つかりません。")
        return

    # 全ファイルを読み込み
    with open(INPUT_FILE, 'r', encoding='utf-8') as f:
        full_text = f.read()

    # 【ファイル名】でセッションごとに分割
    # 正規表現で split する
    sessions = re.split(r'【ファイル名】', full_text)

    # 最初の要素は空の可能性があるので除外
    if not sessions[0].strip():
        sessions = sessions[1:]

    total_sessions = len(sessions)
    print(f"検出されたセッション数: {total_sessions}")

    final_output = "# AI竹尾 ナレッジベース (完成版)\n\n"

    # 各セッションを処理
    for i, session in enumerate(sessions):
        # ファイル名と本文を分離
        # 想定形式: "12-25 Dream.m4a\n===================\n(本文)..."
        parts = session.split('\n', 1)
        file_name = parts[0].strip()
        body = parts[1] if len(parts) > 1 else ""

        if not body.strip():
            continue

        print(f"[{i+1}/{total_sessions}] 処理中: {file_name} ...")

        # 1. 正規表現クリーニング（まずはゴミを減らす）
        cleaned_body = clean_text_regex(body)

        # 2. AIによる構造化（APIキーがある場合）
        structured_content = process_session_with_ai(file_name, cleaned_body)

        final_output += structured_content + "\n"

        # Rate Limit対策（連続リクエストによるエラーを防ぐため少し待機）
        if USE_AI:
            time.sleep(4)

    # 結果を保存
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        f.write(final_output)

    print(f"\n--- 完了！ファイルを作成しました: {OUTPUT_FILE} ---")

if __name__ == "__main__":
    main()