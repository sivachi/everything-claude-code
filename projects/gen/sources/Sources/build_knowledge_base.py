def clean_text_regex(text):
    """
    正規表現による強力なノイズ除去（前処理）
    AIに投げる前に、明らかなゴミを削除してトークンを節約する
    """
    lines = text.split('\n')
    cleaned_lines = []

    # 削除対象のパターン（行全体）
    remove_patterns_line = [
        r"^うん[。、]?$", r"^うんうん[。、]?$", r"^ああ[。、]?$",
        r"^えっと[。、]?$", r"^えー[。、]?$", r"^あのー[。、]?$",
        r"^はい[。、]?$", r"^なるほど[。、]?$", r"^そうですね[。、]?$",
        r"^録音[。、]?$", r"^録画[。、]?$", r"^スタート[。、]?$"
    ]

    # 文中の置換対象
    replace_patterns = [
        (r"^えっと、", ""), (r"^あの、", ""), (r"^ま、", ""),
        (r"うん。うん。", ""), (r"ああ、", "")
    ]

    for line in lines:
        line = line.strip()
        if not line: continue # 空行スキップ

        # 行削除判定
        if any(re.match(p, line) for p in remove_patterns_line):
            continue

        # 文中置換
        for p, r in replace_patterns:
            line = re.sub(p, r, line)

        if line:
            cleaned_lines.append(line)

    return "\n".join(cleaned_lines)

